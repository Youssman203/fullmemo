export const getPopularCollections = () => {
  return [...publicCollections]
    .sort((a, b) => b.popularity - a.popularity)
    .slice(0, 4);
};

export const getRecentCollections = () => {
  return [...publicCollections]
    .sort(() => 0.5 - Math.random())
    .slice(0, 4);
};

export const getCollectionsByCategory = (categoryId) => {
  if (categoryId === 'all') {
    return publicCollections;
  }
  return publicCollections.filter(collection => collection.category === categoryId);
};

export const searchCollections = (query) => {
  const lowerCaseQuery = query.toLowerCase();
  return publicCollections.filter(collection => 
    collection.title.toLowerCase().includes(lowerCaseQuery) || 
    collection.description.toLowerCase().includes(lowerCaseQuery) ||
    collection.author.toLowerCase().includes(lowerCaseQuery) ||
    collection.categoryLabel.toLowerCase().includes(lowerCaseQuery)
  );
};
